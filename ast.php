    <h1>Applied Simulation Technologies</h1>
   
   <div id="left">
   <p>AST is a virtual training technology company specializing in simulation-based training and related software. The company’s unique approach to virtual training involves full scale, immersive, interactive experiences in a simulated environment. AST develops and sells training software, applications packages, and services to the commercial vehicle, law enforcement, homeland security, corporate, and academic communities. AST’s emergency vehicle training program, EVOC-101 has been adopted by Utah as the standard for training their police officers. In addition to developing its own virtual training technology, AST is a Value Added Reseller (VAR) and content provider for simulators manufactured by L-3 Communications, Raydon and Advanced Interactive Systems.</p>
 
<p>The company’s expertise includes adapting technology for synthetic training, driving simulator training and R & D applications, simulator sickness mitigation, systems integration, training environment scenario development, scenario traffic control, vehicle dynamics, tire and road modeling and real time implementation and student acceptance of synthetic driving environments. AST is a professionally licensed engineering firm.</p>

<h1>History</h1>

<p>Since the mid-1980s, AST personnel have been actively researching and designing advanced simulator technology solutions for all types of high to medium performance vehicle simulators. This began with members of the AST team providing integration support on the Daimler-Benz driving simulator in Berlin, Germany. Related experience includes working closely with Carnegie-Melon University, BMW, Toyota, Nissan, GM and Ford to identify hardware and software solutions for each of these companies’ simulation application needs. </p>

<p>AST personnel have also worked with Eaton Corporation to improve the Transmission Shifter Simulator, and provided engineering and integration support to the Federal Department of Transportation for the National Advanced Driving Simulator (NADS) program. Additional simulator subcontracts performed by AST personnel involved the U.S. Navy landing signal officer (LSO) simulator program. </p>

<p>AST personnel led a simulation team at the Sky Lab facility at the University of Mainz, Germany under Professor Rudolf J. von Baumgarten, M.D. to research and analyze human factors issues that can impact simulator adaptation and transfer of training. These early classification studies were essential in establishing simulator cueing thresholds and functional design criteria. This R&D was fundamental to the successful design and development of the I-Sim PatrolSim, TranSim and Mark II simulators while at I-Sim and General Electric Driver Development. Within two years of product launch, I-Sim simulators captured the majority of the commercial and municipal markets. </p>

<p>AST personnel have successfully provided simulator training support to over 200 law enforcement agencies and commercial training schools. Services included needs analysis, specifications, simulator sickness mitigation, transfer of training techniques, scenario development, driver measurement, behavior correction, and financial analysis of simulator benefits as well as sales and leasing. AST’s support in optimizing the integration of its driving simulators into a training program resulted in a reported reduction of accidents by 25 to 74%.</p>

