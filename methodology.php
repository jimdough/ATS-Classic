    <h1>Targeted Training Methodology</h1>
    <ul>
	<li>Measures driver competence in real time simulator scenario database</li>
	<li>Determines what areas need improvement</li>
	<li>Prioritizes areas of training that need improvement</li>
	<li>Provides guided CBT on-screen training on improvement areas</li>
	<li>Learning is then confirmed in real time scenario database</li>
	<li>The SimMaster simulator is used to assess a driver’s skill levels in a short “Targeted” scenario.</li>
	<li>This determines a driver’s weaknesses by comparing their performance to a set of stored “expert driver” algorithms.</li>
	<li>Remediates the weaknesses via CBT training on the SimMaster.</li>
	<li>Re-assesses the drivers skill levels via a short scenario and determines if weakness has been remediated.</li>
	<ul>
		<li>If yes – drivers moves to next scenario</li>
		<li>If no – driver gets another CBT scenario then re-assessment</li>
	</ul>
    </ul>
    
