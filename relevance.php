    <h1>TransMaster simulator-based training system</h1>
    <h2>Metrics and questions to help determine Value and Return on Investment (ROI) </h2>
    
    <ul>
    	<li><b>Value</b> depends on training efficacy</li>
	<li><b>Efficacy </b> requires relevance (Construct Validity)</li>
	<li><b>Relevance</b> requires defined performance, acceptance & measurement = GOALS</li>
	<li><b>Goals</b> sets standard for technology, lesson plans, software, and personnel</li>
	<li><b>ROI</b> results from behavior change: improved safety, maintenance & fuel economy and attitude</li>
    </ul>
    
    <p><b>These measurements done per class:</b></p>
<ol>
	<li>Note time on and off per student</li>
	<li>Note total accumulated individual student time on</li>
	<li>How much time on simulator before starting range driving</li>
		<ol style="list-style-type:lower-alpha;">
			<li>Compare to non-sim student; </li>
			<li>Is student better prepared for truck</li>
			<li>Does student learn faster in truck due to simulator- </li>
				<ol style="list-style-type:upper-roman;">
					<li>Measure time to perform first range tasks well</li>
					<li>Measure time to first OTR drive</li>
				</ol>
		</ol>
	<li>Do the common introductory lessons (if projected from simulator to overhead screen for all the class to see) improve class time and reduce student preparation for the next lesson?</li>
	<li>How often (average hours per student) is simulator used for improving shifting, coordination, etc. To avoid dropping a student.</li>
	<li>Have student drop outs reduced since use of simulation. Give average %</li>
</ol>    
    
    <p><b>Ask instructors:</b></p>
    <ol>
    	<li>How do you use the simulator</li>
