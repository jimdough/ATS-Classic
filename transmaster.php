    <h1>TransMaster</h1>
    
    <div id="left">
	    <p>The ATS TransMaster™ provides student-paced, expert guided, basic to advanced driver training for non-synchronous heavy truck transmission shifting training.  TransMaster™ exceeds traditional simulator performance by incorporating self-guided, introductory Computer Based Training (CBT), richly textured computer graphics, interactive student practice sessions, simulated training scenarios, immediate feedback, guided best practices, audio coaching, adaptive learning assessment, learning management system (LMS) in a reconfigurable driver training shifting simulator.</p>
	    
	    <p>Reconfigurable means that the simulator can be used to train on additional types of vehicles (various class semis, straight trucks, vans, utility vehicles).  This expanded capability is an added value feature to ensure a higher return on investment and minimized obsolescence.  The TransMaster™ is factory-upgradable from its basic 1-channel shifting trainer configuration to a 3-channel driving decision training platform.  This designed growth adapts the simulator both physically and functionally (Figure 1) to expand its performance for a variety of driver training needs.  System growth includes the visual field of view, added steerability, expanded software performance, and additional interactive courseware.</p>
	    
	<ul>
	<li>This hardware platform provides a part-task heavy truck shifting simulator with the ability to be upgraded to a full functioning driving training platform.  </li>
	<li>TransMaster™ incorporates adaptive electronic coaching that guides the student through selected exercises.  This minimizes the need for additional training cadre.</li>
	<li>The driver’s station has a vehicle-like look with an open seat driver’s station. </li>
	<li>The simulator chassis is designed to fit through a standard doorway.</li>
	<li>One robust computer system operates all functions on the simulator; the glass dashboard controls/gages, the shifting simulation; it controls the interactivity of the CBT and allows for future upgrades.</li>
	<li>The Main Display monitor provides a high resolution, real time out the front windshield view of the road.</li>
	<li>The transmission shifter subsystem incorporates new advanced technologies that provide higher reliability with improvements to shift feel and behavior. Gearshift lever and simulated gearing assembly provide realistic shifting feedback, coordinated interaction and sound.</li>
	<li>The air operated seat adjusts positioning for height and distance to the steering wheel. </li>
	<li>The Steering Wheel is placed in the correct ergonomic location and provides a path for future upgrades to a full functioning simulator.</li>
	<li>Reconfigurable dashboard/touch screen using a glass cockpit arrangement which replicates gauges and their placement in various types of vehicles and serves as the input device for the shifting sim.</li>
	<li>New CAN architecture provides Internal I/O circuitry and connections designed to withstand all normal environmental use conditions to include mobile (trailer and RV) and portable environments.</li> 
	<li>Functional and adjustable (foot pressure) brake, clutch and accelerator pedals are provided.</li>
	<li>Remote, wireless modem is provided to access simulator.  An on-board diagnostic program tracks system use, and subsystem performance and functionality.</li>
	<li>A digital sound system simulates normal vehicle operating sounds.</li>
	</ul>
